function renderArchive() {
    const grid = document.getElementById('archive-grid');
    const loadMoreBtn = document.getElementById('load-more-btn');
    if (!grid) return;
    
    const query = document.getElementById('search-bar').value.toLowerCase();
    const todayStr = getSystemDateString();
    
    let controls = document.querySelector('.filter-subnav');
    if (!controls) {
        controls = document.createElement('div');
        controls.className = 'filter-subnav';
        controls.style.cssText = "padding: 15px 5%; display:flex; gap:10px; flex-wrap:wrap; background:var(--grey-light);";
        controls.innerHTML = `
            <button onclick="setArchiveFilter('ALL')" class="filter-tier-btn">ALL BRANDS</button>
            <button onclick="setArchiveFilter('NIKE')" class="filter-tier-btn">NIKE</button>
            <button onclick="setArchiveFilter('JORDAN BRAND')" class="filter-tier-btn">JORDAN</button>
            <button onclick="setArchiveFilter('ADIDAS')" class="filter-tier-btn">ADIDAS</button>
            <button onclick="setArchiveFilter('PUMA')" class="filter-tier-btn">PUMA</button>
            <button onclick="setArchiveFilter('361 DEGREES')" class="filter-tier-btn">361°</button>
            <button onclick="setArchiveFilter('ANTA')" class="filter-tier-btn">ANTA</button>
        `;
        document.getElementById('main-header').appendChild(controls);
    }

    const releasedShoes = shoeDatabase.filter(shoe => shoe.date <= todayStr);
    const matches = releasedShoes.filter(s => {
        const matchesSearch = s.name.toLowerCase().includes(query) || s.brand.toLowerCase().includes(query) || s.colorway.toLowerCase().includes(query) || s.line.toLowerCase().includes(query) || s.athlete.toLowerCase().includes(query);
        const matchesCategory = (currentFilter === "ALL" || s.brand === currentFilter);
        return matchesSearch && matchesCategory;
    });

    grid.innerHTML = '';
    matches.slice(0, visibleItemsCount).forEach(s => grid.appendChild(createCard(s)));
    if (loadMoreBtn) loadMoreBtn.style.display = (visibleItemsCount >= matches.length) ? 'none' : 'block';
    if(matches.length === 0) grid.innerHTML = '<div style="grid-column:1/-1; text-align:center; padding:50px; opacity:0.5; font-weight:900;">NO COMPLIANT HISTORICAL MODELS IDENTIFIED.</div>';
}

