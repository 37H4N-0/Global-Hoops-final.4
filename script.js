// COMPILER ENGINE: MAP ALL EMBEDDED DIVERSE COLORWAYS DIRECTLY INTO ACTIVE MATRIX
const shoeDatabase = [];
let databaseIdCounter = 1;

function compileRealDatabase() {
    let globalIndex = 0;
    for (let signatureTree of signatureLineageTrees) {
        for (let version of signatureTree.versions) {
            for (let colorway of version.colorways) {
                
                const baseModelPrice = 135 + ((version.name.length * 7) % 45);
                const finalTierPrice = Math.round(baseModelPrice / 5) * 5;

                const logYear = 2024 + Math.floor(globalIndex / 30);
                const logMonth = String(1 + (globalIndex % 12)).padStart(2, '0');
                const logDay = String(1 + ((globalIndex * 6) % 28)).padStart(2, '0');
                const formattedLogDate = `${logYear}-${logMonth}-${logDay}`;

                shoeDatabase.push({
                    id: databaseIdCounter++,
                    brand: signatureTree.brand,
                    line: signatureTree.line,
                    name: version.name,
                    athlete: signatureTree.athlete,
                    colorway: colorway.name,
                    price: finalTierPrice,
                    type: signatureTree.type,
                    tech: version.tech,
                    date: formattedLogDate,
                    colorMap: { primary: colorway.primary, secondary: colorway.secondary, text: colorway.text }
                });
                globalIndex++;
            }
        }
    }
}

compileRealDatabase();

